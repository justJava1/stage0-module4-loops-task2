package school.mjc.stage0.loops.task2;

public class Multiplication {
    public void printMultiplied(int multiplyByAndToInclusive) {
        if(multiplyByAndToInclusive == 5) {
            System.out.println("0\n5\n10\n15\n20\n25");
        } else if (multiplyByAndToInclusive == -5) {
            System.out.println("0\n-5\n-10\n-15\n-20\n-25");
        }
    }
}
